import Foundation
import UIKit

extension UIColor {
    class var silverColor: UIColor {
        return UIColor.init(red: 228.0 / 255.0, green: 228.0 / 255.0, blue: 226.0 / 255.0, alpha: 1.0)
    }
    
    class var spaceGrayColor: UIColor {
        return UIColor.init(red: 38.0 / 255.0, green: 37.0 / 255.0, blue: 41.0 / 255.0, alpha: 1.0)
    }
    
    class var goldColor: UIColor {
        return UIColor.init(red: 250.0 / 255.0, green: 220.0 / 255.0, blue: 194.0 / 255.0, alpha: 1.0)
    }

}
